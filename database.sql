-- ==============================================================================
-- Student Performance Predictor DB Schema (Oracle Format)
-- ==============================================================================

-- 1. STUDENTS Table
-- Stores the base student profile information
CREATE TABLE STUDENTS (
    ROLL_NO VARCHAR2(20) PRIMARY KEY,
    STUDENT_NAME VARCHAR2(100) NOT NULL,
    PHONE_NUMBER VARCHAR2(15),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 2. ATTENDANCE Table
-- Stores daily attendance records for students
CREATE TABLE ATTENDANCE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLL_NO VARCHAR2(20) NOT NULL,
    ATTENDANCE_DATE DATE NOT NULL,
    STATUS VARCHAR2(10) NOT NULL CHECK (STATUS IN ('present', 'absent')),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_attendance_rollno FOREIGN KEY (ROLL_NO) 
        REFERENCES STUDENTS(ROLL_NO) ON DELETE CASCADE,
    CONSTRAINT uq_attendance_daily UNIQUE (ROLL_NO, ATTENDANCE_DATE)
);

-- 3. ASSESSMENTS Table
-- Stores the marks and assessments records
CREATE TABLE ASSESSMENTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLL_NO VARCHAR2(20) NOT NULL,
    ASSESSMENT_TYPE VARCHAR2(50) NOT NULL,
    SUBJECT VARCHAR2(150) NOT NULL,
    SCORE NUMBER(5, 2) NOT NULL,
    MAX_SCORE NUMBER(5, 2) NOT NULL,
    ASSESSMENT_DATE DATE DEFAULT SYSDATE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_assessments_rollno FOREIGN KEY (ROLL_NO) 
        REFERENCES STUDENTS(ROLL_NO) ON DELETE CASCADE
);

